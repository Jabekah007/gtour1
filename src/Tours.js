import React from 'react';
import Tour from './Tour';
/**
 * We're mapping over the tours array and returning a Tour component for each tour
 * @returns The tours component is being returned.
 */
const Tours = ({ tours, removeTour }) => {
  return (
    <section>
      <div className="title">
        <h2>our tours</h2>
        <div className="underline"></div>
      </div>
      <div>
        {tours.map((tour) => {
          return <Tour key={tour.id} {...tour} removeTour={removeTour} />;
        })}
      </div>
    </section>
  );
};

export default Tours;
